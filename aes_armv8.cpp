    std::array<uint8_t, 16> ct_raw;

    // 1. 初始化密钥
    uint8x16_t roundkeys[15];
    AES256_keyschedule(vld1q_u8(key_raw.data()), vld1q_u8(key_raw.data() + 16), roundkeys);

    // 2. 加密
    uint8x16_t data = vld1q_u8(pt_raw.data());
